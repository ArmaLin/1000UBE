package com.dyaco.spirit_commercial.support.custom_view;

import android.util.Log;

public class BarLocation {
    int x;
    int y;

    public BarLocation(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
