    public int getPwmViaLevel(int level) {
        // TODO: 取得level相對應的pwm  (依設定的PWMs)
        try {
            if (levelAD == null || levelAD.length == 0)
                return 0;

            level = Math.max(level, 1);
            level = Math.min(level, levelAD.length);
            return levelAD[level - 1];
        } catch (Exception e) {
            Timber.d( "exception = %s", e.getMessage());
            return 0;
        }
    }


